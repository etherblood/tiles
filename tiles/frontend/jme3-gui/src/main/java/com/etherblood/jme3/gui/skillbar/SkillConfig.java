package com.etherblood.jme3.gui.skillbar;

public class SkillConfig {

    public int id;
    public String iconPath;
    public String name;
    public Integer index;

    public SkillConfig(int id, String iconPath, String name, Integer index) {
        this.id = id;
        this.iconPath = iconPath;
        this.name = name;
        this.index = index;
    }

}
